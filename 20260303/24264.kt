/*
 * 문제: 알고리즘 수업 - 알고리즘의 수행 시간 3 (백준 24264)
 *
 * 입력의 크기 n이 주어지면 MenOfPassion 알고리즘의 코드1 수행 횟수와
 * 최고차항의 차수를 출력하는 문제.
 *
 * MenOfPassion(A[], n) {
 *     sum <- 0;
 *     for i <- 1 to n
 *         for j <- 1 to n
 *             sum <- sum + A[i] × A[j]; # 코드1
 *     return sum;
 * }
 *
 * ── 풀이 계획 ──
 * 1. i가 1~n, j가 1~n이므로 코드1은 n × n = n² 번 수행된다.
 * 2. 수행 횟수를 다항식으로 나타내면 n² → 최고차항의 차수는 2이다.
 * 3. n이 최대 500,000이므로 n²은 long(Long) 범위로 처리해야 한다.
 *    (500,000² = 250,000,000,000 → Int 범위 초과, Long 필요)
 *
 * ── 시간복잡도 ──
 * O(1) : 단순히 n²을 계산하고 차수 2를 출력하기만 하면 되므로 상수 시간.
 *
 * ── 사용 알고리즘 ──
 * 특별한 알고리즘 없이, 이중 반복문의 수행 횟수가 n²임을 수학적으로 도출.
 */

fun main() {
    // readLine()!! : 표준 입력에서 한 줄을 읽음. null이 아님을 보장(!!)
    // toLong() : 문자열을 Long 타입으로 변환
    // trim() : 입력 앞뒤의 공백/개행 문자를 제거
    val n = readLine()!!.trim().toLong()

    // n * n : 이중 for문(1~n, 1~n)이므로 코드1 수행 횟수는 n²
    println(n * n)

    // 다항식 n²의 최고차항 차수는 2
    println(2)
}
