/**
 * 문제 제목: 쿠폰 (백준 10179번)
 *
 * 문제 내용:
 * 쇼핑몰에서 할인 쿠폰을 제공한다. 이 쿠폰은 정가에서 20%를 할인해준다.
 * 여러 개의 물건 가격이 주어질 때, 각 물건에 쿠폰을 적용한 가격을 출력한다.
 *
 * 입력:
 * - 첫 번째 줄에 테스트 케이스의 수 T가 주어진다.
 * - 다음 T줄에 각각 물건의 정가가 주어진다. (소수점 포함)
 *
 * 출력:
 * - 각 테스트 케이스에 대해 "$XX.XX" 형식으로 할인된 가격을 출력한다.
 *
 * ---
 *
 * 풀이 계획:
 * 1. 테스트 케이스 수 T를 읽는다.
 * 2. T번 반복하면서 가격을 읽고, 0.8을 곱해 20% 할인 적용
 * 3. "%.2f".format()을 사용하여 소수점 2자리까지 출력
 *
 * ---
 *
 * 시간복잡도: O(T)
 * - T개의 테스트 케이스를 순차적으로 처리하므로 O(T)
 * - 각 케이스에서 곱셈과 출력은 O(1)
 */

// main 함수: 프로그램의 진입점
fun main() {
    // readLine()!!: 표준 입력에서 한 줄을 읽음. !!는 null이 아님을 단언하는 연산자
    // .trim(): 앞뒤 공백 제거
    // .toInt(): 문자열을 정수로 변환
    val t = readLine()!!.trim().toInt()

    // repeat(t): t번 반복 실행하는 코틀린 표준 라이브러리 함수
    // 알고리즘: 단순 반복 - 각 테스트 케이스를 독립적으로 처리
    repeat(t) {
        // .toDouble(): 문자열을 실수(Double)로 변환
        val price = readLine()!!.trim().toDouble()

        // 20% 할인 = 원래 가격의 80% = price * 0.8
        val discounted = price * 0.8

        // "%.2f".format(value): 소수점 이하 2자리까지 반올림하여 포맷팅
        // 문자열 템플릿 ${}를 사용하여 달러 기호와 함께 출력
        // println(): 줄바꿈을 포함하여 출력
        println("${"$"}${"%.2f".format(discounted)}")
    }
}
