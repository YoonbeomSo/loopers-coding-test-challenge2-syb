/**
 * 백준 25643 - 문자열 탑 쌓기
 *
 * [문제]
 * N개의 길이 M인 문자열이 주어진다.
 * 문자열을 아래에서부터 순서대로 쌓아 탑을 만든다.
 * 새 문자열을 쌓을 때, 바로 아래 문자열과 최소 1칸 이상 겹쳐야 하며,
 * 겹치는 부분의 문자가 모두 동일해야 한다.
 * 수평 방향으로 좌우 이동(오프셋)이 가능하고, 오프셋 d는 -(M-1) ~ (M-1) 범위이다.
 * 모든 연속 쌍에 대해 유효한 오프셋이 하나라도 존재하면 탑을 쌓을 수 있다(1 출력),
 * 그렇지 않으면 쌓을 수 없다(0 출력).
 *
 * [풀이 계획]
 * 1. 각 연속 문자열 쌍 (A, B)에 대해 오프셋 d를 -(M-1)부터 (M-1)까지 순회한다.
 * 2. 오프셋 d일 때, B가 A 대비 d만큼 오른쪽으로 이동한 상태이다.
 *    겹치는 열 범위: max(0, d) ~ min(M-1, d+M-1)
 *    → 열 c에서 A[c]와 B[c-d]가 같아야 한다.
 * 3. 겹치는 모든 문자가 같은 오프셋이 하나라도 있으면 해당 쌍은 유효하다.
 * 4. 모든 쌍이 유효하면 1, 하나라도 불가능하면 0을 출력한다.
 *
 * [시간복잡도]
 * O(N × M²)
 * - N-1개의 연속 쌍 각각에 대해 최대 2M-1개의 오프셋을 시도하고,
 *   각 오프셋에서 최대 M개의 문자를 비교한다.
 * - N=100, M=100일 때 최대 약 2,000,000 연산으로 충분히 빠르다.
 */

// main 함수: 프로그램의 진입점
fun main() {
    // BufferedReader를 사용하여 빠른 입력 처리
    val br = System.`in`.bufferedReader()

    // 첫 줄: N(문자열 개수)과 M(문자열 길이)을 읽음
    // split(" "): 공백 기준으로 문자열을 분리하여 리스트로 반환
    // map { it.toInt() }: 각 문자열 원소를 Int로 변환
    val (n, m) = br.readLine().split(" ").map { it.toInt() }

    // N개의 문자열을 리스트로 읽음
    // List(n) { ... }: 크기 n인 리스트를 람다로 초기화
    val strings = List(n) { br.readLine() }

    // [알고리즘: 완전탐색 (Brute Force)]
    // 모든 연속 쌍에 대해 유효한 오프셋이 존재하는지 확인한다.
    // 연속 쌍 (i, i+1)을 순회
    for (i in 0 until n - 1) {
        val a = strings[i]     // 아래쪽 문자열 (이미 쌓여 있는 꼭대기)
        val b = strings[i + 1] // 위에 새로 쌓을 문자열

        // 현재 쌍에서 유효한 오프셋이 있는지 추적하는 플래그
        var found = false

        // 오프셋 d를 -(M-1)부터 (M-1)까지 순회
        // d > 0: B가 A보다 오른쪽으로 이동
        // d < 0: B가 A보다 왼쪽으로 이동
        // d = 0: 완전히 겹침
        for (d in -(m - 1)..(m - 1)) {
            // 겹치는 열 범위 계산
            // start: 겹침 시작 열 (A 기준 인덱스)
            // end: 겹침 끝 열 (A 기준 인덱스)
            // maxOf, minOf: 두 값 중 큰/작은 값을 반환하는 코틀린 표준 함수
            val start = maxOf(0, d)
            val end = minOf(m - 1, d + m - 1)


            // 겹치는 영역의 모든 문자가 일치하는지 확인
            // all { ... }: Iterable의 모든 원소가 조건을 만족하면 true 반환
            // start..end: IntRange, 닫힌 범위 (start 이상 end 이하)
            val match = (start..end).all { c ->
                // A의 c번째 문자와 B의 (c-d)번째 문자 비교
                a[c] == b[c - d]
            }

            // 유효한 오프셋을 찾으면 더 이상 탐색하지 않음
            if (match) {
                found = true
                break // 내부 for문 탈출
            }
        }

        // 현재 쌍에서 유효한 오프셋이 없으면 탑 쌓기 불가능
        if (!found) {
            println(0)
            return // main 함수 종료
        }
    }

    // 모든 쌍이 유효하면 탑 쌓기 가능
    println(1)
}
